# 숫자형 : 숫자 형태로 이루어진 자료형
# 종류 : 정수, 실수 8진수, 16진수
# 정수형과 실수형은 따로 선언하는 것 없이 숫자를 입력하면 파이썬에서 자동으로 인식해준다
# 8진수를 사용하려면 숫자 앞에 0o처럼 숫자0+알파벳 대문자O or 소문자o를 입력하고,
# 16진수를 사용하려면 숫자 앞에 0x처럼 숫자0+알파벳 대문자X or 소문자x를 입력하면 된다.
# 파이썬에서는 사칙연산도 가능하며 제곱도 표현할 수 있다
# a=2, b=3 이고, a**b를 할경우 8이 출력된다.
# /는 값을 //는 몫을 %는 나머지를 출력한다.

a=2
b=3
print(a**b)
print(10*(18**2)+2*11)
print(14//3)
print(14%3)